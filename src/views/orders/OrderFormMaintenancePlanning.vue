          const b64 = f.target.result
          this.documents.push({
            file: b64,
            name: files[i].name,
            description: ''
          })
        }

        reader.readAsDataURL(files[i])
      }
    },
    async deleteDocument(index) {
      const deleted = this.documents.splice(index, 1)
      try {
        for (const document of deleted) {
